places_to_know = [
    "italia",
    "brazil",
    "roma",
    "eua",
    "canada",
]

print(places_to_know)
print("SORTED LIST BELOW")
print(sorted(places_to_know))
print(places_to_know)
print("SORTED LIST REVERSED BELOW")
print(sorted(places_to_know, reverse=True))
print(places_to_know)
print("RESERVED LIST BELOW")
places_to_know.reverse()
print(places_to_know)
print("Reversed back to normal")
places_to_know.reverse()
print(places_to_know)
print("Lista alterada para ordenada")
places_to_know.sort()
print(places_to_know)
print("lista ordenada de forma inversa")
places_to_know.sort(reverse=True)
print(places_to_know)
